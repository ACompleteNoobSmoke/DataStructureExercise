public class Animal{
    private String petName;
    private String petType;

    public void setPetName(String p_name){
        this.petName = p_name;
    }
    public String getPetName(){
        return petName;
    }

    public void setPetType(String p_Type){
        this.petType = p_Type;
    }

    public String getPetType(){
        return petType;
    }
}